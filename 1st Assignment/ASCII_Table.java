import java.util.*;
public class ASCII_Table {
public static void main (String []args){
System.out.println( "For 'A' to 'Z' :" );
for ( int i = 'A' ; i<= 'Z' ; i++){
System.out.println(( char )(i)+ "-" +i+ " " );
}
System.out.println( "For 'a' to 'z' :" );
for ( int i = 'a' ; i<= 'z' ; i++){
System.out.println(( char )(i)+ "-" +i+ " " );
}
System.out.println( "For '0' to '9' :" );
for ( int i = '0' ; i<= '9' ; i++){
System.out.println(( char )(i)+ "-" +i+ " " );
}
}
}
